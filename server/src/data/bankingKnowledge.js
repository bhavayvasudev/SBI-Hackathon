export function retrieveRelevantKnowledge(query, topK = 3) {
  const queryLower = query.toLowerCase();
  const scored = bankingKnowledge.map(doc => {
    let score = 0;
    for (const kw of doc.keywords) {
      if (queryLower.includes(kw)) score += 3;
    }
    const words = queryLower.split(/\s+/);
    for (const word of words) {
      if (word.length > 3 && doc.content.toLowerCase().includes(word)) score += 1;
      if (word.length > 3 && doc.title.toLowerCase().includes(word)) score += 2;
    }
    return { ...doc, score };
  });

  return scored
    .filter(d => d.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
    .map(d => `### ${d.title}\n${d.content}`)
    .join('\n\n');
}
